
## Problem Description 
# Given two binary values `x` and `y`, compute the following bitwise operations. 

# 1.Bitwise AND (&)
# 2.Bitwise OR (|)
# 3.Bitwise NOT (~) of 'x'

# In the the **solution.py** file, create three variables `ans_and`,`ans_or` and `ans_not`
#  to store the resulting values of `x` and `y`, `x` or `y` and not `x` respectively.

x = 0b0110
y = 0b1000

ans_and = x & y
ans_or = x|y
ans_not = ~x